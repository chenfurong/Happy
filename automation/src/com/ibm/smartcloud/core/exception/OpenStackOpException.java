package com.ibm.smartcloud.core.exception;

@SuppressWarnings("serial")
public class OpenStackOpException extends BaseException {

	public OpenStackOpException() {
	}

	public OpenStackOpException(String arg0) {
		super(arg0);
	}

	public OpenStackOpException(Throwable arg0) {
		super(arg0);
	}

	
	public OpenStackOpException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
